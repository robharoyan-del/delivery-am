type Keys = keyof typeof T.en

interface I18nCtx {
  lang: Lang
  setLang: (l: Lang) => void
  t: (key: Keys, vars?: Record<string, string>) => string
}

const I18nContext = createContext<I18nCtx>({
  lang: 'en',
  setLang: () => {},
  t: (key) => key,
})

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>('en')

  useEffect(() => {
    const saved = localStorage.getItem('lang') as Lang | null
    if (saved && saved in T) setLangState(saved)
  }, [])

  function setLang(l: Lang) {
    setLangState(l)
    localStorage.setItem('lang', l)
  }

  function t(key: Keys, vars?: Record<string, string>): string {
    let str: string = (T[lang] as Record<string, string>)[key] ?? (T.en as Record<string, string>)[key] ?? key
    if (vars) {
      Object.entries(vars).forEach(([k, v]) => { str = str.replace(`{${k}}`, v) })
    }
    return str
  }

  return <I18nContext.Provider value={{ lang, setLang, t }}>{children}</I18nContext.Provider>
}

export function useLanguage() {
  return useContext(I18nContext)
}
